	 PROGRAM EXTHREE
IMPLICIT NONE
 INTEGER :: EQUIPS; REAL :: UCOST
 READ*, EQUIPS,UCOST
 
 SELECT CASE(EQUIPS)
  CASE(1:9)
    IF(UCOST>=0 .AND. UCOST<=10.00)THEN
    PRINT*, UCOST,0,UCOST
  
    ELSE IF(UCOST>=10.01 .AND. UCOST<=100.00)THEN
    PRINT*, UCOST, (UCOST*2/100), UCOST-(UCOST*2/100)
  
    ELSE
    PRINT*, UCOST, (UCOST*5/100), UCOST-(UCOST*5/100)
	END IF

  CASE(10:19)
    IF(UCOST>=0 .AND. UCOST<=10.00)THEN
    PRINT*, UCOST, (UCOST*5/100), UCOST-(UCOST*5/100)
  
    ELSE IF(UCOST>=10.01 .AND. UCOST<=100.00)THEN
    PRINT*, UCOST, (UCOST*7/100), UCOST-(UCOST*7/100)
  
    ELSE
    PRINT*, UCOST, (UCOST*9/100), UCOST-(UCOST*9/100)
    END IF

  CASE(20:49)
    IF(UCOST>=0 .AND. UCOST<=10.00)THEN
    PRINT*, UCOST,(UCOST*9/100),UCOST-(UCOST*9/100)
  
    ELSE IF(UCOST>=10.01 .AND. UCOST<=100.00)THEN
    PRINT*, UCOST,(UCOST*15/100),UCOST-(UCOST*15/100)
  
    ELSE
    PRINT*, UCOST,(UCOST*21/100),UCOST-(UCOST*21/100)
    END IF

  CASE(50:99)
    IF(UCOST>=0 .AND. UCOST<=10.00)THEN
    PRINT*, UCOST,(UCOST*14/100),UCOST-(UCOST*14/100)
  
    ELSE IF(UCOST>=10.01 .AND. UCOST<=100.00)THEN
    PRINT*, UCOST,(UCOST*23/100),UCOST-(UCOST*23/100)
  
    ELSE
    PRINT*, UCOST,(UCOST*32/100),UCOST-(UCOST*32/100)
    END IF

  CASE DEFAULT
    IF(UCOST>=0 .AND. UCOST<=10.00)THEN
    PRINT*, UCOST,(UCOST*21/100),UCOST-(UCOST*21/100)
  
    ELSE IF(UCOST>=10.01 .AND. UCOST<=100.00)THEN
    PRINT*, UCOST,(UCOST*32/100),UCOST-(UCOST*32/100)
  
    ELSE
    PRINT*, UCOST,(UCOST*43/100),UCOST-(UCOST*43/100)
    END IF
  END SELECT

 END PROGRAM EXTHREE
