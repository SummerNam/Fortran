PROGRAM EX
 
IMPLICIT NONE
INTEGER :: I,N
REAL :: AVGT,SUMT=0.0,AVGW,SUMW=0.0,MAXT=0,MINT=999,MAXW=0,MINW=999
REAL,DIMENSION(8,720) :: D

OPEN(11,FILE="C:\201309.txt")
READ(11,*)D 

PRINT*, "         TEMPERATURE", "       WINDSPEED"

  N=(24*30)
  DO I=1,N
  SUMT=D(6,I)+SUMT		
  AVGT=SUMT/N  
  END DO
 
  DO I=1,N
  SUMW=D(8,I)+SUMW
  AVGW=SUMW/N
  END DO

 PRINT*, "AVG.",AVGT,AVGW


 DO I=1,N
    IF(D(6,I)<=MINT) THEN
	MINT=D(6,I)
	END IF
	
	IF(D(8,I)<=MINW) THEN
	MINW=D(8,I)
	END IF
 END DO

 PRINT*, "MIN.", MINT, MINW


 DO I=1,N
    IF(D(6,I)>=MAXT) THEN
	MAXT=D(6,I)
	END IF
	IF(D(8,I)>=MAXW) THEN
	MAXW=D(8,I)
	END IF
 END DO

 PRINT*, "MAX.", MAXT, MAXW

CLOSE(11)

END PROGRAM
